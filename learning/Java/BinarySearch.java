    static int binarySearch(int arr[], int n) {
        int low = 0;
        int high = arr.length - 1;
        int mid;

        while (low <= high) {
            mid = low + (high - low) / 2;

            if (arr[mid] > n) high = mid - 1;

            else if (arr[mid] < n) low = mid + 1;

            else return mid;
        }

        return -1;
    }
